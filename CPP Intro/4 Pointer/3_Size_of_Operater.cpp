#include <iostream>
using namespace std;
int main(){
    std::cout<<"Size of integer data type :"<<sizeof(int)<<std::endl;
    std::cout<<"Size of float data type :"<<sizeof(float)<<std::endl;
    std::cout<<"Size of double data data type :"<<sizeof(double)<<std::endl;
    std::cout<<"Size of character data type :"<<sizeof(char)<<std::endl;
    return 0;
}